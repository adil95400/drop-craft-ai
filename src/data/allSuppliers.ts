// Helpers
export const getSuppliersByCategory = (category: string): UnifiedSupplier[] => {
  if (category === 'all') return ALL_SUPPLIERS;
  return ALL_SUPPLIERS.filter(s => s.category === category);
};

export const getSuppliersByCountry = (country: string): UnifiedSupplier[] => {
  if (country === 'all') return ALL_SUPPLIERS;
  return ALL_SUPPLIERS.filter(s => s.country === country || s.regions?.includes(country));
};

export const getPopularSuppliers = (): UnifiedSupplier[] => {
  return ALL_SUPPLIERS.filter(s => s.isPopular);
};

export const getNewSuppliers = (): UnifiedSupplier[] => {
  return ALL_SUPPLIERS.filter(s => s.isNew);
};

export const searchSuppliers = (query: string): UnifiedSupplier[] => {
  const lowerQuery = query.toLowerCase();
  return ALL_SUPPLIERS.filter(s => 
    s.name.toLowerCase().includes(lowerQuery) ||
    s.description.toLowerCase().includes(lowerQuery) ||
    s.features.some(f => f.toLowerCase().includes(lowerQuery)) ||
    s.category.toLowerCase().includes(lowerQuery)
  );
};

export const getSupplierById = (id: string): UnifiedSupplier | undefined => {
  return ALL_SUPPLIERS.find(s => s.id === id);
};

// Stats
export const getSupplierStats = () => {
  const totalProducts = ALL_SUPPLIERS.reduce((sum, s) => sum + (s.productsCount || 0), 0);
  const categories = new Set(ALL_SUPPLIERS.map(s => s.category));
  const countries = new Set(ALL_SUPPLIERS.map(s => s.country));
  
  return {
    totalSuppliers: ALL_SUPPLIERS.length,
    totalProducts,
    totalCategories: categories.size,
    totalCountries: countries.size,
    popularSuppliers: ALL_SUPPLIERS.filter(s => s.isPopular).length,
    newSuppliers: ALL_SUPPLIERS.filter(s => s.isNew).length,
  };
};
